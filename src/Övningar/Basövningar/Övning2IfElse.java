package Övningar.Basövningar;

public class Övning2IfElse
{
    public static void main(String[] args)
    {
        int number1 = 3;
        int number2 = 3;

        if (number1 > number2)
        {
            System.out.println("Hej!");
        }
        if (number1 == number2)
        {
            System.out.println("Hej då!");
        }



        int number3 = 4;
        int number4 = 8;

        if (number3 > number4)
        {
            System.out.println("Hej snygging!");
        }
        else if (number3 == number4)
        {
            System.out.println("Va?");
        }

        else
        {
            System.out.println("Hej då snygging!");
        }



        System.out.println("Slut!");


    }
}
