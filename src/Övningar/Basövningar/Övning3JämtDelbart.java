package Övningar.Basövningar;

public class Övning3JämtDelbart {
    public static void main(String[] args)
    {
        int number1 = 14;
        int number2 = 2;

        if (number1 % number2 == 0) //Om number1 delat med number2 får resten 0
        {
            System.out.println("Number1 är jämnt delbart med number2!");
        }
        else
        {
            System.out.println("Number1 är inte jämnt delbart med number2!");
        }


    }


}